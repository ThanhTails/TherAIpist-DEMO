function changeSearchBar(){
    var sbhide = document.getElementById("sbhide");
    var sbhide2 = document.getElementById("sbhide2");
    var sbhide3 = document.getElementById("sbhide3");
    var btnhide = document.getElementById("btnhide");

    sbhide.style.display = "block";
    sbhide2.style.display = "block";
    sbhide3.style.display = "block";
    btnhide.style.display = "none";    
}

